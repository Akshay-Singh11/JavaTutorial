public class Odd_Even {
    public static void main(String[] args) {
        int n = 72;
        if (n >= 50) {
            System.out.println("Hii");
        }

        if (n % 2 == 0) {
            System.out.println("Even");
        } else {
            System.out.println("Odd");
        }

    }

}
